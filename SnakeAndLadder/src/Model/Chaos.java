package Model;

public abstract class Chaos {
	int startPos;
	int finalPos;
	public int getStartPos() {
		return startPos;
	}
	public void setStartPos(int startPos) {
		this.startPos = startPos;
	}
	public int getFinalPos() {
		return finalPos;
	}
	public void setFinalPos(int value) {
		this.finalPos = value;
	}
}
